# arithmetic operator : 산술연산자 (수치형 데이터를 가지고 진행하는 일반 산술 연산)
# +, -, *, /, % , **, //
print(13 + 5)
print(13 - 5)
print(13 * 5)
print(13 / 5)   # 나누기 계산
print(13 % 5)
print(13 // 5)  # 몫 계산
print(2 ** 8)  # 제곱
print()
# bitwise operator : 비트 연산
# &(and) , | (or), ^ (xor),  ~(not) , << (left shift), >> (right shift)
print(3 & 5, 3 | 5, 3 ^ 5, 3 << 1, 3 >> 1)

# assignment operator
# =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ~=, ^=, >>=, <<=
d = 10
d += 3
d /= 4
print(d)
